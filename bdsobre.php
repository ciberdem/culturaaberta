				        	<td style="padding-left: 10px">
				        		<div id="texto1"><img src="img/logo_cultura_aberta.png" style="float:right; padding-left: 10px;" width="`140" height="140">O <b>Portal Cultura Aberta</b> foi criado com o propósito de disponibilizar, de forma unificada e estruturada ao cidadão interessado em cultura, informações à respeito das OSCIPs de cultura da cidade do Rio de Janeiro. As OSCIPs (Organização da Sociedade Civil de Interesse Público) são organizações não governamentais (ONGs) que possuem como característica principal a prerrogativa de realizar convênios e parcerias com órgãos públicos para maior eficácia na execução de Políticas Públicas.
								</div>
				        		<p>
					        		
				        		<div id="texto1">O Portal Cultura Aberta é dividido nas seguintes seções:</div>
				        		<p id="texto1">
				        		
								<a href="./" id="link1" style="padding: 0px">HOME</a>: Página de entrada, apresentando uma breve descrição do site, links rápidos de destaque para projetos e notícias referentes às OSCIPs.<br>
								<a href="./projetos.php" id="link1" style="padding: 0px">PROJETOS E CONVÊNIOS</a>: Informações referentes aos projetos e convênios disponibilizados pelas OSCIPs. A principal diferença de um convênio pra um projeto é que o convênio é obrigatoriamente resultante de uma parceria (formalizada via contrato) do Estado com uma OSCIP para a execução de um projeto.<br>
								<a href="./noticias.php" id="link1" style="padding: 0px">NOTÍCIAS</a>: Notícias referentes às OSCIPs.<br>
								<a href="./editais.php" id="link1" style="padding: 0px">EDITAIS E PARTICIPAÇÃO</a>: São disponibilizadas informações de como participar ou colaborar com as OSCIPs, via voluntariado, doações ou como oportunidade de trabalho. Além disso, são disponibilizados links para editais e sites de cultura.<br>
								<a href="./dados.php" id="link1" style="padding: 0px">DADOS ABERTOS</a>: As informações das OSCIPs, projetos, convênios e formas de participação são disponibilizadas através de arquivos em formato aberto (texto, XML, HTML e JSON).</p>
								
								<div id="texto1"><b>Observações importantes</b>:</div>
								<div id="texto1"><li>Os dados disponibilizados no Portal Cultura Aberta são resultado de um processo semiautomático de extração na web de informações referentes às OSCIPs (sites oficiais, redes sociais, sites de ONGs, entre outras). O último processo de extração foi realizado em 02/02/2018, logo, algumas informações poderão estar desatualizadas.</li>
								<li>De acordo com a estruturação planejada para o site, caso uma informação não tenha sido encontrada na web, então será exibido “(Fonte não encontrada)”.</li>
